#include <rand.h>


/*************************************************************************************************************
*                                              LOCAL TABLES                                                  *
*************************************************************************************************************/
static unsigned int Seed = 1; 


/*************************************************************************************************************
*                                            FUNCTION PROTOTYPES                                             *
*************************************************************************************************************/
void SetSeed (unsigned int s) 
{ 
    Seed = s; 
}

unsigned int GetRand (void) 
{ 
    Seed = MULTIPLIER * Seed + INCREMENT; 
    return ((unsigned int)(Seed>>8) & 0x7fff); 
}


