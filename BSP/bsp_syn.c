/*************************************************************************************************************
*                                            FUNCTION PROTOTYPES                                             *
*************************************************************************************************************/
CPU_BOOL BspSyn (void)
{
    CPU_INT8U n;
    CPU_INT8U syn;
    
    syn = 0x00;
    /* Wait for Syn complete*/
    n = 0;
    while (1)
    {
        n++;
        if (n == BSP_SYN_WAIT)
        {
            return DEF_FALSE;
        }
        
        syn <<= 1;
        syn |= BspSynGet(); 
        if ((syn & BSP_SYN_MASK) == BSP_SYN_ON)
        {
            break;
        }
        BspTimerDelayUS(50);
    }
    
    return DEF_TRUE;
}


